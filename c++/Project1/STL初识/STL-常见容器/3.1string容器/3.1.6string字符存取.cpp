#include<iostream>
using namespace std;
#include<string>

//string字符存取
void test01()
{
    string str="hello";

    //cout<<"str="<<str<<endl;

    //1.通过[]访问单个字符
    for(int i=0;i<5;i++)  //i.size()
    {
        cout<<str[i]<<' ';
    }
    cout<<endl;

    //2.通过at的方式访问单个字符
    for(int i=0;i<5;i++)
    {
        cout<<str.at(i)<<' ';
    }
    cout<<endl;

    //修改单个字符
    str[0]='x';
    cout<<"str="<<str<<endl;

    str.at(1)='x';
    cout<<"str="<<str<<endl;
}


int main()
{
    test01();
    return 0;
}
